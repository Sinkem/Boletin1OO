public class Triangulo {

    private double base, altura;

    public Triangulo() {
        base = 2;
        altura = 3;
    }

    public double getBase() {
        return this.base;
    }

    public void setBase(double base) {
        this.base = base;
    }

    public double getAltura() {
        return this.altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }

}